// Print numbers from 1 to 10
for (let i = 1; i <= 10; i++) {
    console.log(i);
}

// Print even numbers from 1 to 20
for (let i = 2; i <= 20; i += 2) {
    console.log(i);
}

// Star pattern
for (let i = 1; i <= 4; i++) {
    let pattern = "";
    for (let j = 1; j <= i; j++) {
        pattern += "* ";
    }
    console.log(pattern);
}
